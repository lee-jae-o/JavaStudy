public class Study10 {
    public static void main(String[] args) {
        int a = 125;
        for (int i = 0; i < 5; i++) {
            a++;
            System.out.println("a: " + a);
        }

        System.out.println("----------------------");

        int b = -125;
        for (int i = 0; i < 5; i++){
            b--;
            System.out.println("b: " + b);
        }
    }
}
